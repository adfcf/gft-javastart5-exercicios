public class Main
{
public static void main(String[] args) {

        /*
        * ESTRUTURAS ITERATIVAS DE CONTROLE DE FLUXO 
        */
        
        boolean condicao = true;
        
        // Estrutura iterativa pré-condicionada (while)
        while (condicao) {
            // ...
        }
		
		// Estrutura iterativa pós-condicionada (do-while)
        do {
            // ...
        } while (condicao);
		
		// Estrutura iterativa enumerada (for)
        for (int i = 0; i < 10; ++i) {
            // ...
        }
		
}
}
